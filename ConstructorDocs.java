package constructor;

public class ConstructorDocs {
	
	/*
	 * --------------Constructor---------------
	 * 	# used to create object in java.
	 * 	# special type of method which has same name as class name.
	 * 	# no return type.
	 * 	# if there is no any user defined constructor in class then java provides default constructor 
	 * to create constructor.
	 * 
	 * # Types
	 * 	
	 * 1. Default Constructor :
	 * 	# used to create objects and set default values to object.
	 * 
	 * 		# syntax:
	 * 
	 * 			class_name(){
	 * 				// set default values to object
	 * 		
	 * 		}
	 * 2. Parameterized Constructor :
	 * 	# use to create object and set dynamic values to object.
	 * 	
	 * 	# syntax :
	 * 		
	 * 		class_name(arg1,arg2,.....,argn){
	 * 				//set dynamic values to object
	 * 		
	 * 		}
	 * 
	 */
	
	
	
}
